def bootstrap_metric_ci(y_true, probs, num_classes, n_bootstrap=1000, seed=42):
    rng = np.random.default_rng(seed)
    n = len(y_true)

    metric_names = [
        "accuracy",
        "balanced_accuracy",
        "f1_macro",
        "f1_weighted",
        "mcc",
        "cohen_kappa",
        "roc_auc_macro_ovr",
        "pr_auc_macro",
        "log_loss",
        "brier_score",
        "ece_15bins"
    ]

    boot_values = {m: [] for m in metric_names}

    y_true = np.asarray(y_true)
    probs = np.asarray(probs)

    for _ in tqdm(range(n_bootstrap), desc="Bootstrap", leave=False):
        idx = rng.integers(0, n, size=n)

        # Skip samples missing a class for AUC stability, but compute others.
        yt = y_true[idx]
        pr = probs[idx]

        metrics = compute_metrics_from_probs(yt, pr, num_classes)

        for m in metric_names:
            boot_values[m].append(metrics.get(m, np.nan))

    rows = []
    for m in metric_names:
        arr = np.asarray(boot_values[m], dtype=float)
        arr = arr[~np.isnan(arr)]

        if len(arr) == 0:
            rows.append({
                "metric": m,
                "mean": np.nan,
                "ci95_lower": np.nan,
                "ci95_upper": np.nan,
                "n_bootstrap_valid": 0
            })
        else:
            rows.append({
                "metric": m,
                "mean": float(np.mean(arr)),
                "ci95_lower": float(np.percentile(arr, 2.5)),
                "ci95_upper": float(np.percentile(arr, 97.5)),
                "n_bootstrap_valid": int(len(arr))
            })

    return pd.DataFrame(rows)

