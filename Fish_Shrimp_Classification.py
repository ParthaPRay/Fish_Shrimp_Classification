    wrong_df = pred_df[pred_df["correct"] == 0].copy()

    selected = []

    if len(correct_df) > 0:
        idxs = rng.choice(
            correct_df.index.values,
            size=min(max_correct, len(correct_df)),
            replace=False
        )
        selected.extend([(int(i), "correct") for i in idxs])

    if len(wrong_df) > 0:
        idxs = rng.choice(
            wrong_df.index.values,
            size=min(max_wrong, len(wrong_df)),
            replace=False
        )
        selected.extend([(int(i), "misclassified") for i in idxs])

    return selected


