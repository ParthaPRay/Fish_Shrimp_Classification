def build_soft_voting_ensemble(domain_name, top_k=3):
    domain_summary = summary_df[summary_df["domain"] == domain_name].copy()
    domain_summary = domain_summary.sort_values("f1_macro", ascending=False)

    top_models = domain_summary["model_key"].head(top_k).tolist()
    print(f"{domain_name} top-{top_k} ensemble models:", top_models)

    pred_dfs = []
    for model_key in top_models:
        pred_dfs.append(all_results[domain_name][model_key]["predictions"].copy())

    y_true = pred_dfs[0]["y_true"].values
    prob_cols = [c for c in pred_dfs[0].columns if c.startswith("prob_class_")]

    probs_stack = np.stack([df[prob_cols].values for df in pred_dfs], axis=0)
    probs_mean = probs_stack.mean(axis=0)

    y_pred = probs_mean.argmax(axis=1)
    num_classes = probs_mean.shape[1]
    id2label = domain_data[domain_name]["id2label"]

    metrics = compute_metrics_from_probs(y_true, probs_mean, num_classes)
    metrics.update({
        "domain": domain_name,
        "model_key": f"Hybrid_SoftVoting_Top{top_k}",
        "family": "Hybrid Ensemble",
        "source": "Post-hoc soft voting",
        "num_classes": num_classes,
        "ensemble_members": ", ".join(top_models)
    })

    pred_df = pd.DataFrame({
        "y_true": y_true,
        "y_pred": y_pred
    })

    for c in range(num_classes):
        pred_df[f"prob_class_{c}"] = probs_mean[:, c]

    pred_df["true_label_name"] = pred_df["y_true"].map(id2label)
    pred_df["pred_label_name"] = pred_df["y_pred"].map(id2label)
    pred_df["correct"] = (pred_df["y_true"] == pred_df["y_pred"]).astype(int)

    return metrics, pred_df


