def paired_mcnemar_comparisons(domain_name, all_predictions):
    """
    Compare every model against the best model using paired McNemar test.
    Best model is selected by test macro-F1 from summary table later.
    """

    rows = []

    # Compute macro-F1 for each model from saved predictions
    model_scores = {}
    for model_key, pred_df in all_predictions.items():
        y_true = pred_df["y_true"].values
        y_pred = pred_df["y_pred"].values
        model_scores[model_key] = f1_score(y_true, y_pred, average="macro", zero_division=0)

    best_model_key = max(model_scores, key=model_scores.get)
    best_df = all_predictions[best_model_key]
    best_correct = best_df["correct"].values.astype(int)

    for model_key, pred_df in all_predictions.items():
        if model_key == best_model_key:
            continue

        other_correct = pred_df["correct"].values.astype(int)

        # McNemar contingency:
        # b = best correct, other wrong
        # c = best wrong, other correct
        b = int(((best_correct == 1) & (other_correct == 0)).sum())
        c = int(((best_correct == 0) & (other_correct == 1)).sum())

        table = [[0, b], [c, 0]]

        try:
            result = mcnemar(table, exact=True)
            stat = float(result.statistic)
            p_value = float(result.pvalue)
        except Exception:
            stat = np.nan
            p_value = np.nan

        rows.append({
            "domain": domain_name,
            "best_model": best_model_key,
            "comparison_model": model_key,
            "best_correct_other_wrong_b": b,
            "best_wrong_other_correct_c": c,
            "mcnemar_statistic": stat,
            "mcnemar_exact_p_value": p_value,
            "significant_at_0_05": bool(p_value < 0.05) if not np.isnan(p_value) else None
        })

    return pd.DataFrame(rows)


def cochrans_q_multiple_model_test(domain_name, all_predictions):
    """
    Cochran's Q test checks whether multiple classifiers have equal accuracy
    on the same test instances.
    """

    model_keys = list(all_predictions.keys())
    correctness_matrix = np.column_stack([
        all_predictions[m]["correct"].values.astype(int) for m in model_keys
    ])

    try:
        q_result = cochrans_q(correctness_matrix)
        q_stat = float(q_result.statistic)
        p_value = float(q_result.pvalue)
    except Exception as e:
        q_stat = np.nan
        p_value = np.nan

    return pd.DataFrame([{
        "domain": domain_name,
        "models_compared": ", ".join(model_keys),
        "cochrans_q_statistic": q_stat,
        "cochrans_q_p_value": p_value,
        "significant_at_0_05": bool(p_value < 0.05) if not np.isnan(p_value) else None
    }])

