def denormalize_tensor_image(tensor_img):
    """
    Convert normalized torchvision tensor image back to PIL-like RGB array.
    Assumes ImageNet normalization.
    """
    mean = torch.tensor([0.485, 0.456, 0.406]).view(3, 1, 1)
    std = torch.tensor([0.229, 0.224, 0.225]).view(3, 1, 1)

    img = tensor_img.detach().cpu() * std + mean
    img = img.clamp(0, 1)
    img = img.permute(1, 2, 0).numpy()
    return img


def tensor_to_rgb_numpy(tensor_img):
    """
    General fallback converter for tensor image to RGB numpy.
    """
    img = tensor_img.detach().cpu()

    if img.ndim == 3 and img.shape[0] == 3:
        img = img.permute(1, 2, 0).numpy()

    img = img.astype(np.float32)

    if img.min() < 0 or img.max() > 1:
        img = img - img.min()
        img = img / (img.max() + 1e-8)

    return img


def make_heatmap_overlay(rgb_img, heatmap, alpha=0.38):
    """
    Overlay heatmap on RGB image.
    """
    rgb_img = np.asarray(rgb_img).astype(np.float32)

    if rgb_img.max() <= 1:
        rgb_img_255 = (rgb_img * 255).astype(np.uint8)
    else:
        rgb_img_255 = rgb_img.astype(np.uint8)

    heatmap = np.asarray(heatmap).astype(np.float32)
    heatmap = heatmap - heatmap.min()
    heatmap = heatmap / (heatmap.max() + 1e-8)

    heatmap_uint8 = np.uint8(255 * heatmap)
    heatmap_color = cv2.applyColorMap(heatmap_uint8, cv2.COLORMAP_JET)
    heatmap_color = cv2.cvtColor(heatmap_color, cv2.COLOR_BGR2RGB)

    overlay = cv2.addWeighted(rgb_img_255, 1 - alpha, heatmap_color, alpha, 0)
    return overlay


