def get_torchvision_target_layer(model, model_name):
    """
    Select final convolutional feature layer for Grad-CAM.
    """
    if model_name == "resnet50":
        return model.layer4[-1]

    elif model_name == "efficientnet_b0":
        return model.features[-1]

    elif model_name == "mobilenet_v3_large":
        return model.features[-1]

    elif model_name == "densenet121":
        return model.features[-1]

    elif model_name == "convnext_tiny":
        return model.features[-1]

    else:
        raise ValueError(f"No Grad-CAM target layer defined for {model_name}")


class GradCAM:
    """
    Minimal Grad-CAM implementation for torchvision CNN models.
    """

    def __init__(self, model, target_layer):
        self.model = model
        self.target_layer = target_layer
        self.activations = None
        self.gradients = None

        self.forward_handle = self.target_layer.register_forward_hook(self.save_activation)
        self.backward_handle = self.target_layer.register_full_backward_hook(self.save_gradient)

    def save_activation(self, module, input, output):
        self.activations = output.detach()

    def save_gradient(self, module, grad_input, grad_output):
        self.gradients = grad_output[0].detach()

    def __call__(self, input_tensor, class_idx=None):
        self.model.zero_grad(set_to_none=True)

        logits = self.model(input_tensor)

        if class_idx is None:
            class_idx = int(torch.argmax(logits, dim=1).item())

        score = logits[:, class_idx].sum()
        score.backward(retain_graph=True)

        gradients = self.gradients
        activations = self.activations

        weights = gradients.mean(dim=(2, 3), keepdim=True)
        cam = (weights * activations).sum(dim=1, keepdim=True)
        cam = F.relu(cam)

        cam = F.interpolate(
            cam,
            size=input_tensor.shape[-2:],
            mode="bilinear",
            align_corners=False
        )

        cam = cam.squeeze().detach().cpu().numpy()
        cam = cam - cam.min()
        cam = cam / (cam.max() + 1e-8)

        return cam, logits.detach()

    def remove_hooks(self):
        self.forward_handle.remove()
        self.backward_handle.remove()


def hf_attention_heatmap(model, pixel_values, model_name):
    """
    Attention visualization for ViT-like Hugging Face models.

    For ViT models, this extracts last-layer CLS-to-patch attention.
    For Swin models, attentions may not map directly to image patches,
    so this function returns a coarse averaged attention map when available.
    """
    model.eval()

    with torch.no_grad():
        outputs = model(
            pixel_values=pixel_values,
            output_attentions=True,
            return_dict=True
        )

    logits = outputs.logits
    pred_class = int(torch.argmax(logits, dim=1).item())

    if outputs.attentions is None:
        raise ValueError("No attentions returned by this Hugging Face model.")

    last_attn = outputs.attentions[-1]

    # ViT attention shape: [B, heads, tokens, tokens]
    if last_attn.ndim == 4:
        attn = last_attn[0].detach().cpu()

        # Case 1: ViT-style CLS attention
        if attn.shape[-1] >= 197:
            cls_attn = attn.mean(dim=0)[0, 1:]  # CLS to patches
            n_patches = cls_attn.numel()
            grid_size = int(np.sqrt(n_patches))

            if grid_size * grid_size == n_patches:
                heatmap = cls_attn.reshape(grid_size, grid_size).numpy()
            else:
                heatmap = cls_attn.numpy().reshape(1, -1)

        # Case 2: Swin-like window attention fallback
        else:
            heatmap = attn.mean(dim=0).mean(dim=0).numpy()
            if heatmap.ndim == 1:
                side = int(np.sqrt(heatmap.size))
                if side * side == heatmap.size:
                    heatmap = heatmap.reshape(side, side)
                else:
                    heatmap = heatmap.reshape(1, -1)

    else:
        raise ValueError(f"Unsupported attention shape: {last_attn.shape}")

    heatmap = heatmap.astype(np.float32)
    heatmap = heatmap - heatmap.min()
    heatmap = heatmap / (heatmap.max() + 1e-8)

    heatmap = cv2.resize(
        heatmap,
        (pixel_values.shape[-1], pixel_values.shape[-2]),
        interpolation=cv2.INTER_CUBIC
    )

    return heatmap, logits.detach(), pred_class


