@torch.no_grad()
def evaluate_model(model, loader, criterion, num_classes):
    model.eval()

    losses = []
    y_true_all = []
    probs_all = []

    start = time.perf_counter()

    if torch.cuda.is_available():
        torch.cuda.synchronize()
        torch.cuda.reset_peak_memory_stats()

    for batch in tqdm(loader, leave=False):
        pixel_values = batch["pixel_values"].to(DEVICE, non_blocking=True)
        labels = batch["labels"].to(DEVICE, non_blocking=True)

        logits = forward_logits(model, pixel_values)
        loss = criterion(logits, labels)

        probs = safe_softmax(logits)

        losses.append(loss.item())
        y_true_all.extend(labels.detach().cpu().numpy().tolist())
        probs_all.extend(probs.detach().cpu().numpy())

    if torch.cuda.is_available():
        torch.cuda.synchronize()

    elapsed = time.perf_counter() - start
    n_images = len(y_true_all)

    probs_all = np.asarray(probs_all)
    y_true_all = np.asarray(y_true_all)
    y_pred_all = probs_all.argmax(axis=1)

    metrics = compute_metrics_from_probs(y_true_all, probs_all, num_classes)
    metrics["loss"] = float(np.mean(losses))
    metrics["inference_total_sec"] = elapsed
    metrics["inference_ms_per_image"] = 1000.0 * elapsed / max(n_images, 1)
    metrics["inference_images_per_sec"] = n_images / max(elapsed, 1e-9)

    res = get_resource_snapshot()
    metrics.update({f"inference_{k}": v for k, v in res.items()})

    return metrics, y_true_all, y_pred_all, probs_all

