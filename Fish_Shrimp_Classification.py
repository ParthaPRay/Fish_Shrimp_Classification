def train_one_model(domain_name, model_spec, domain_info):
    domain_ds = domain_info["dataset"]
    id2label = domain_info["id2label"]
    label2id = domain_info["label2id"]
    num_classes = len(id2label)

    model_key = model_spec["model_key"]
    model_name = model_spec["model_name"]
    model_source = model_spec["source"]

    safe_key = clean_model_name(model_key)

    print("\n" + "=" * 100)
    print(f"Domain: {domain_name}")
    print(f"Model: {model_name}")
    print(f"Classes: {num_classes}")
    print("=" * 100)

    # --------------------------------------------------------
    # Dataloaders
    # --------------------------------------------------------
    train_loader, valid_loader, test_loader = make_dataloaders(
        domain_ds=domain_ds,
        model_spec=model_spec,
        batch_size=BATCH_SIZE
    )

    # --------------------------------------------------------
    # Model
    # --------------------------------------------------------
    model = build_model(
        model_spec=model_spec,
        num_labels=num_classes,
        id2label=id2label,
        label2id=label2id
    )
    model.to(DEVICE)

    # --------------------------------------------------------
    # Class weights
    # --------------------------------------------------------
    class_weights, class_counts = get_class_weights(domain_ds, num_classes)

    if USE_CLASS_WEIGHTS:
        criterion = nn.CrossEntropyLoss(weight=class_weights.to(DEVICE))
    else:
        criterion = nn.CrossEntropyLoss()

    # --------------------------------------------------------
    # Optimizer and scheduler
    # --------------------------------------------------------
    lr = LR_TRANSFORMER if model_source == "HuggingFace" else LR_CNN

    optimizer = torch.optim.AdamW(
        model.parameters(),
        lr=lr,
        weight_decay=WEIGHT_DECAY
    )

    total_update_steps = math.ceil(len(train_loader) / GRAD_ACCUM_STEPS) * EPOCHS
    warmup_steps = int(WARMUP_RATIO * total_update_steps)

    scheduler = get_cosine_schedule_with_warmup(
        optimizer,
        num_warmup_steps=warmup_steps,
        num_training_steps=total_update_steps
    )

    scaler = torch.cuda.amp.GradScaler(
        enabled=(USE_AMP and DEVICE.type == "cuda")
    )

    # --------------------------------------------------------
    # Output directories
    # --------------------------------------------------------
    model_dir = MODEL_ROOT / domain_name / safe_key
    fig_dir = FIG_ROOT / domain_name / safe_key
    table_dir = TABLE_ROOT / domain_name / safe_key
    pred_dir = PRED_ROOT / domain_name / safe_key

    for p in [model_dir, fig_dir, table_dir, pred_dir]:
        p.mkdir(parents=True, exist_ok=True)

    with open(model_dir / "id2label.json", "w") as f:
        json.dump({str(k): v for k, v in id2label.items()}, f, indent=2)

    # --------------------------------------------------------
    # Training state
    # --------------------------------------------------------
    history = []
    best_val_f1 = -np.inf
    best_epoch = -1
    epochs_without_improvement = 0

    # Minimum improvement required to save a new best model
    MIN_DELTA = 1e-4

    training_start = time.perf_counter()

    # ========================================================
    # Epoch loop
    # ========================================================
    for epoch in range(1, EPOCHS + 1):
        model.train()
        epoch_start = time.perf_counter()

        if torch.cuda.is_available():
            torch.cuda.synchronize()
            torch.cuda.reset_peak_memory_stats()

        train_losses = []
        train_y_true = []
        train_probs = []

        optimizer.zero_grad(set_to_none=True)

        for step, batch in enumerate(
            tqdm(train_loader, desc=f"Epoch {epoch}/{EPOCHS}", leave=False),
            start=1
        ):
            pixel_values = batch["pixel_values"].to(DEVICE, non_blocking=True)
            labels = batch["labels"].to(DEVICE, non_blocking=True)

            with torch.cuda.amp.autocast(
                enabled=(USE_AMP and DEVICE.type == "cuda")
            ):
                logits = forward_logits(model, pixel_values)
                loss = criterion(logits, labels)
                loss = loss / GRAD_ACCUM_STEPS

            scaler.scale(loss).backward()

            if step % GRAD_ACCUM_STEPS == 0 or step == len(train_loader):
                scaler.unscale_(optimizer)
                torch.nn.utils.clip_grad_norm_(
                    model.parameters(),
                    MAX_GRAD_NORM
                )

                scaler.step(optimizer)
                scaler.update()
                scheduler.step()
                optimizer.zero_grad(set_to_none=True)

            with torch.no_grad():
                probs = safe_softmax(logits)

            train_losses.append(loss.item() * GRAD_ACCUM_STEPS)
            train_y_true.extend(labels.detach().cpu().numpy().tolist())
            train_probs.extend(probs.detach().cpu().numpy())

        if torch.cuda.is_available():
            torch.cuda.synchronize()

        train_probs = np.asarray(train_probs)
        train_y_true = np.asarray(train_y_true)

        train_metrics = compute_metrics_from_probs(
            train_y_true,
            train_probs,
            num_classes
        )
        train_metrics["loss"] = float(np.mean(train_losses))

        # ----------------------------------------------------
        # Validation
        # ----------------------------------------------------
        valid_metrics, valid_y_true, valid_y_pred, valid_probs = evaluate_model(
            model=model,
            loader=valid_loader,
            criterion=criterion,
            num_classes=num_classes
        )

        epoch_time = time.perf_counter() - epoch_start
        resource = get_resource_snapshot()

        row = {
            "domain": domain_name,
            "model_key": model_key,
            "model_name": model_name,
            "epoch": epoch,
            "lr": scheduler.get_last_lr()[0],
            "epoch_time_sec": epoch_time,

            "train_loss": train_metrics["loss"],
            "train_accuracy": train_metrics["accuracy"],
            "train_balanced_accuracy": train_metrics["balanced_accuracy"],
            "train_f1_macro": train_metrics["f1_macro"],
            "train_f1_weighted": train_metrics["f1_weighted"],
            "train_mcc": train_metrics["mcc"],

            "valid_loss": valid_metrics["loss"],
            "valid_accuracy": valid_metrics["accuracy"],
            "valid_balanced_accuracy": valid_metrics["balanced_accuracy"],
            "valid_f1_macro": valid_metrics["f1_macro"],
            "valid_f1_weighted": valid_metrics["f1_weighted"],
            "valid_mcc": valid_metrics["mcc"],

            **resource
        }

        history.append(row)

        print(
            f"Epoch {epoch:02d} | "
            f"Train loss={row['train_loss']:.4f}, "
            f"F1m={row['train_f1_macro']:.4f} | "
            f"Val loss={row['valid_loss']:.4f}, "
            f"F1m={row['valid_f1_macro']:.4f}, "
            f"Acc={row['valid_accuracy']:.4f} | "
            f"Time={epoch_time:.1f}s"
        )

        # ----------------------------------------------------
        # Save best model by validation macro-F1
        # ----------------------------------------------------
        current_val_f1 = valid_metrics["f1_macro"]

        if current_val_f1 > best_val_f1 + MIN_DELTA:
            best_val_f1 = current_val_f1
            best_epoch = epoch
            epochs_without_improvement = 0

            if model_source == "HuggingFace":
                model.save_pretrained(model_dir / "best_model")

                processor = AutoImageProcessor.from_pretrained(model_name)
                processor.save_pretrained(model_dir / "best_model")

            else:
                torch.save(
                    {
                        "model_state_dict": model.state_dict(),
                        "model_name": model_name,
                        "num_classes": num_classes,
                        "id2label": id2label,
                        "label2id": label2id,
                        "model_spec": model_spec
                    },
                    model_dir / "best_model.pt"
                )

            with open(model_dir / "best_model_info.json", "w") as f:
                json.dump(
                    {
                        "domain": domain_name,
                        "model_key": model_key,
                        "model_name": model_name,
                        "best_epoch": best_epoch,
                        "best_valid_f1_macro": best_val_f1,
                        "id2label": {str(k): v for k, v in id2label.items()}
                    },
                    f,
                    indent=2
                )

        else:
            epochs_without_improvement += 1

        if epochs_without_improvement >= PATIENCE:
            print(f"Early stopping at epoch {epoch}. Best epoch = {best_epoch}")
            break

    # ========================================================
    # Save training history
    # ========================================================
    total_train_time = time.perf_counter() - training_start

    history_df = pd.DataFrame(history)
    history_df.to_csv(table_dir / "training_history.csv", index=False)
    history_df.to_excel(table_dir / "training_history.xlsx", index=False)

    # ========================================================
    # Reload best model before test evaluation
    # ========================================================
    del model
    gc.collect()

    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    if model_source == "HuggingFace":
        model = AutoModelForImageClassification.from_pretrained(
            model_dir / "best_model"
        )
        model.to(DEVICE)

    else:
        model = build_torchvision_model(model_name, num_classes)
        checkpoint = torch.load(model_dir / "best_model.pt", map_location=DEVICE)
        model.load_state_dict(checkpoint["model_state_dict"])
        model.to(DEVICE)

    # ========================================================
    # Test evaluation
    # ========================================================
    test_metrics, test_y_true, test_y_pred, test_probs = evaluate_model(
        model=model,
        loader=test_loader,
        criterion=criterion,
        num_classes=num_classes
    )

    test_metrics.update(
        {
            "domain": domain_name,
            "model_key": model_key,
            "model_name": model_name,
            "family": model_spec["family"],
            "source": model_spec["source"],
            "num_classes": num_classes,
            "best_epoch": best_epoch,
            "best_valid_f1_macro": best_val_f1,
            "total_train_time_sec": total_train_time,
            "trainable_parameters": sum(
                p.numel() for p in model.parameters() if p.requires_grad
            ),
            "total_parameters": sum(p.numel() for p in model.parameters())
        }
    )

    # ========================================================
    # Save predictions
    # ========================================================
    pred_df = pd.DataFrame(
        {
            "y_true": test_y_true,
            "y_pred": test_y_pred
        }
    )

    for c in range(num_classes):
        pred_df[f"prob_class_{c}"] = test_probs[:, c]

    pred_df["true_label_name"] = pred_df["y_true"].map(id2label)
    pred_df["pred_label_name"] = pred_df["y_pred"].map(id2label)
    pred_df["correct"] = (
        pred_df["y_true"] == pred_df["y_pred"]
    ).astype(int)

    pred_df.to_csv(pred_dir / "test_predictions.csv", index=False)
    pred_df.to_excel(pred_dir / "test_predictions.xlsx", index=False)

    # ========================================================
    # Classification report
    # ========================================================
    report_dict = classification_report(
        test_y_true,
        test_y_pred,
        target_names=[id2label[i] for i in range(num_classes)],
        output_dict=True,
        zero_division=0
    )

    report_df = pd.DataFrame(report_dict).T
    report_df.to_csv(table_dir / "test_classification_report.csv")
    report_df.to_excel(table_dir / "test_classification_report.xlsx")

    print("\nClassification report:")
    print(report_df.round(4).to_markdown())

    # ========================================================
    # Raw confusion matrix
    # ========================================================
    cm = confusion_matrix(
        test_y_true,
        test_y_pred,
        labels=list(range(num_classes))
    )

    cm_df = pd.DataFrame(
        cm,
        index=[id2label[i] for i in range(num_classes)],
        columns=[id2label[i] for i in range(num_classes)]
    )

    cm_df.to_csv(table_dir / "test_confusion_matrix.csv")
    cm_df.to_excel(table_dir / "test_confusion_matrix.xlsx")

    print("\nRaw confusion matrix table:")
    print(cm_df.to_markdown())

    print("\nCSV-style raw confusion matrix:")
    print(cm_df.to_csv())

    # ========================================================
    # Normalized row-wise confusion matrix
    # ========================================================
    cm_float = cm.astype(float)
    row_sums = cm_float.sum(axis=1, keepdims=True)

    cm_norm = np.divide(
        cm_float,
        row_sums,
        out=np.zeros_like(cm_float),
        where=row_sums != 0
    )

    cm_norm_df = pd.DataFrame(
        cm_norm,
        index=[id2label[i] for i in range(num_classes)],
        columns=[id2label[i] for i in range(num_classes)]
    ).round(4)

    cm_norm_df.to_csv(table_dir / "test_confusion_matrix_normalized.csv")
    cm_norm_df.to_excel(table_dir / "test_confusion_matrix_normalized.xlsx")

    print("\nNormalized confusion matrix table:")
    print(cm_norm_df.to_markdown())

    print("\nCSV-style normalized confusion matrix:")
    print(cm_norm_df.to_csv())

    # ========================================================
    # Save test metrics
    # ========================================================
    test_metrics_df = pd.DataFrame([test_metrics])
    test_metrics_df.to_csv(table_dir / "test_metrics_summary.csv", index=False)
    test_metrics_df.to_excel(table_dir / "test_metrics_summary.xlsx", index=False)

    print("\nTest metrics summary:")
    print(test_metrics_df.round(4).to_markdown(index=False))

    # ========================================================
    # Cleanup
    # ========================================================
    del model
    gc.collect()

    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    return {
        "history": history_df,
        "test_metrics": test_metrics,
        "predictions": pred_df,
        "classification_report": report_df,
        "confusion_matrix": cm_df,
        "confusion_matrix_normalized": cm_norm_df,
        "id2label": id2label,
        "table_dir": table_dir,
        "fig_dir": fig_dir,
        "pred_dir": pred_dir,
        "model_dir": model_dir
    }

