def build_domain_dataset(raw_ds, domain_name, prefix):
    """
    Filter the original 11-class dataset into a domain-specific dataset:
    - Fish_ -> 7 classes
    - Shrimp_ -> 4 classes

    Then remap labels into contiguous values: 0, 1, ..., C-1.
    """

    domain_ds = DatasetDict()

    for split in raw_ds.keys():
        domain_ds[split] = raw_ds[split].filter(
            lambda x: x["label_name"].startswith(prefix)
        )

    # Build original label -> name mapping from train split
    label_pairs = {}
    for item in domain_ds["train"]:
        label_pairs[int(item["label"])] = item["label_name"]

    original_labels_sorted = sorted(label_pairs.keys())
    id2label = {new_i: label_pairs[old_i] for new_i, old_i in enumerate(original_labels_sorted)}
    label2id = {v: k for k, v in id2label.items()}
    old2new = {old_i: new_i for new_i, old_i in enumerate(original_labels_sorted)}

    def remap_label(example):
        example["original_label"] = int(example["label"])
        example["label"] = int(old2new[int(example["label"])])
        example["domain"] = domain_name
        return example

    for split in domain_ds.keys():
        domain_ds[split] = domain_ds[split].map(remap_label)

    return domain_ds, id2label, label2id, old2new


