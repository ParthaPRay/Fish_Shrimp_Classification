class HFDiseaseImageDataset(Dataset):
    """
    Dataset wrapper for Hugging Face image processors.
    """

    def __init__(self, hf_dataset, processor):
        self.ds = hf_dataset
        self.processor = processor

    def __len__(self):
        return len(self.ds)

    def __getitem__(self, idx):
        item = self.ds[int(idx)]
        image = item["image"].convert("RGB")
        label = int(item["label"])

        processed = self.processor(images=image, return_tensors="pt")
        pixel_values = processed["pixel_values"].squeeze(0)

        return {
            "pixel_values": pixel_values,
            "labels": torch.tensor(label, dtype=torch.long),
            "label_name": item["label_name"]
        }


class TorchvisionDiseaseImageDataset(Dataset):
    """
    Dataset wrapper for torchvision CNN models.
    """

    def __init__(self, hf_dataset, transform):
        self.ds = hf_dataset
        self.transform = transform

    def __len__(self):
        return len(self.ds)

    def __getitem__(self, idx):
        item = self.ds[int(idx)]
        image = item["image"].convert("RGB")
        label = int(item["label"])

        pixel_values = self.transform(image)

        return {
            "pixel_values": pixel_values,
            "labels": torch.tensor(label, dtype=torch.long),
            "label_name": item["label_name"]
        }


def make_torchvision_transforms(train=True, img_size=224):
    if train:
        return transforms.Compose([
            transforms.Resize((img_size, img_size)),
            transforms.RandomHorizontalFlip(p=0.5),
            transforms.RandomRotation(degrees=10),
            transforms.ColorJitter(
                brightness=0.12,
                contrast=0.12,
                saturation=0.12,
                hue=0.03
            ),
            transforms.ToTensor(),
            transforms.Normalize(
                mean=[0.485, 0.456, 0.406],
                std=[0.229, 0.224, 0.225]
            )
        ])
    else:
        return transforms.Compose([
            transforms.Resize((img_size, img_size)),
            transforms.ToTensor(),
            transforms.Normalize(
                mean=[0.485, 0.456, 0.406],
                std=[0.229, 0.224, 0.225]
            )
        ])


def make_dataloaders(domain_ds, model_spec, batch_size=16):
    """
    Returns train, valid, test dataloaders for either:
    - Hugging Face models
    - torchvision CNN models
    """

    if model_spec["source"] == "HuggingFace":
        processor = AutoImageProcessor.from_pretrained(model_spec["model_name"])

        train_dataset = HFDiseaseImageDataset(domain_ds["train"], processor)
        valid_dataset = HFDiseaseImageDataset(domain_ds["valid"], processor)
        test_dataset  = HFDiseaseImageDataset(domain_ds["test"], processor)

    elif model_spec["source"] == "torchvision":
        train_tf = make_torchvision_transforms(train=True, img_size=IMG_SIZE)
        eval_tf = make_torchvision_transforms(train=False, img_size=IMG_SIZE)

        train_dataset = TorchvisionDiseaseImageDataset(domain_ds["train"], train_tf)
        valid_dataset = TorchvisionDiseaseImageDataset(domain_ds["valid"], eval_tf)
        test_dataset  = TorchvisionDiseaseImageDataset(domain_ds["test"], eval_tf)

    else:
        raise ValueError("Unknown model source")

    train_loader = DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=0,
        pin_memory=True
    )

    valid_loader = DataLoader(
        valid_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=0,
        pin_memory=True
    )

    test_loader = DataLoader(
        test_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=0,
        pin_memory=True
    )

    return train_loader, valid_loader, test_loader

