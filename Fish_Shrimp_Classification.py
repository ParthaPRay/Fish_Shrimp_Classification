def build_hf_model(model_name, num_labels, id2label, label2id):
    """
    Build a Hugging Face image classification model.

    The original pretrained classification head is replaced by a new
    task-specific classification head with num_labels outputs.
    ignore_mismatched_sizes=True is required because ImageNet models
    usually have 1000 output classes, while our fish/shrimp tasks have
    7 or 4 classes.
    """

    hf_id2label = {int(k): v for k, v in id2label.items()}
    hf_label2id = {v: int(k) for k, v in id2label.items()}

    model = AutoModelForImageClassification.from_pretrained(
        model_name,
        num_labels=num_labels,
        id2label=hf_id2label,
        label2id=hf_label2id,
        ignore_mismatched_sizes=True
    )

    return model


def build_torchvision_model(model_name, num_labels):
    """
    Build torchvision CNN models with ImageNet pretrained weights
    and replace the final classifier layer according to num_labels.
    """

    if model_name == "resnet50":
        weights = models.ResNet50_Weights.IMAGENET1K_V2
        model = models.resnet50(weights=weights)
        in_features = model.fc.in_features
        model.fc = nn.Linear(in_features, num_labels)

    elif model_name == "efficientnet_b0":
        weights = models.EfficientNet_B0_Weights.IMAGENET1K_V1
        model = models.efficientnet_b0(weights=weights)
        in_features = model.classifier[1].in_features
        model.classifier[1] = nn.Linear(in_features, num_labels)

    elif model_name == "mobilenet_v3_large":
        weights = models.MobileNet_V3_Large_Weights.IMAGENET1K_V2
        model = models.mobilenet_v3_large(weights=weights)
        in_features = model.classifier[3].in_features
        model.classifier[3] = nn.Linear(in_features, num_labels)

    elif model_name == "densenet121":
        weights = models.DenseNet121_Weights.IMAGENET1K_V1
        model = models.densenet121(weights=weights)
        in_features = model.classifier.in_features
        model.classifier = nn.Linear(in_features, num_labels)

    elif model_name == "convnext_tiny":
        weights = models.ConvNeXt_Tiny_Weights.IMAGENET1K_V1
        model = models.convnext_tiny(weights=weights)
        in_features = model.classifier[2].in_features
        model.classifier[2] = nn.Linear(in_features, num_labels)

    else:
        raise ValueError(f"Unknown torchvision model: {model_name}")

    return model


def build_model(model_spec, num_labels, id2label, label2id):
    """
    Unified model builder.

    It dispatches to:
    1. Hugging Face model builder for ViT, Swin, Falconsai-ViT, etc.
    2. Torchvision model builder for CNN models such as ResNet50,
       EfficientNet-B0, MobileNetV3, DenseNet121, ConvNeXt-Tiny.
    """

    if model_spec["source"] == "HuggingFace":
        model = build_hf_model(
            model_name=model_spec["model_name"],
            num_labels=num_labels,
            id2label=id2label,
            label2id=label2id
        )

    elif model_spec["source"] == "torchvision":
        model = build_torchvision_model(
            model_name=model_spec["model_name"],
            num_labels=num_labels
        )

    else:
        raise ValueError(f"Unknown model source: {model_spec['source']}")

    return model


